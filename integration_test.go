func TestIntegration_VerifyDemoLic(t *testing.T) {
	licData, err := os.ReadFile("testdata/demo.lic")
	require.NoError(t, err, "testdata/demo.lic must be generated first")

	pubKeyData, err := os.ReadFile("testdata/public.pem")
	require.NoError(t, err, "testdata/public.pem must be generated first")

	pubKey, err := ParsePublicKey(pubKeyData)
	require.NoError(t, err)

	lic, err := VerifyBytes(licData, pubKey)
	require.NoError(t, err)
	require.NotNil(t, lic)
	require.Equal(t, "demo-lic-001", lic.ID)
	require.Equal(t, "travelog", lic.Product)
	require.Equal(t, LicenseTypeStandard, lic.LicenseType)
	require.Equal(t, "Demo Customer", lic.CustomerName)
	require.Equal(t, 5, lic.MaxDevices)

	// Check features
	require.True(t, lic.IsFeatureEnabled("export_pdf"))
	require.True(t, lic.IsFeatureEnabled("export_csv"))
	require.False(t, lic.IsFeatureEnabled("bulk_import"))

	// Check capabilities
	storage := lic.GetCapability("storage_gb")
	require.Equal(t, float64(500), storage)
	tier := lic.GetCapability("tier")
	require.Equal(t, "professional", tier)

	// Check metadata
	require.Equal(t, "cn-beijing", lic.Metadata["region"])

	// Check validity
	require.False(t, lic.IsExpired())
	require.False(t, lic.IsRevoked())
	require.True(t, lic.IsValid())
}

